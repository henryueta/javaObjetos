package Biblioteca;

public class Livro {
	
	private String codigo_livro;
	
	private String nome;
	

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCodigo_livro() {
		return codigo_livro;
	}

	public void setCodigo_livro(String codigo_livro) {
		this.codigo_livro = codigo_livro;
	}
	
}
